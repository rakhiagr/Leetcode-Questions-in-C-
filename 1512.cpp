int numIdenticalPairs(vector<int> &nums)
{
    int pairs = 0;

    for (int i = 0; i < nums.size() - 1; i++)
    {
        for (int j = i + 1; j < nums.size(); j++)
        {
            if (nums[i] == nums[j])
                pairs++;
        }
    }
    return pairs;
}

int32_t main()
{
    vector<int> vec = {1, 2, 3, 1, 1, 3};
    cout << numIdenticalPairs(vec);
}